// public - so everybody can access it 
// class - this is a class (duh)
// P40MyFirstApp - the name of this class 
// { - opening curly brace of this class
public class P040MyFirstApp {
	// public - so everybody can access it 
	// static - we'll cover this one later
	// void - the return type, void means there's no return type
	// main - the name of this method
	// String[] args - arguments to the method.
	// 	this method must be given array of Strings 
	// 	and the array will be called 'args'
	// { - opening brace of the method
	public static void main(String[] args) {
		// System.out.print.. - this says print to standard 
		// 	output (defaults to command-line)
		// "Hello Java!" - the String You want to print
		// every statement MUST end in a semicolon
		System.out.print("Hello Java!");
	// closing brace of the 'main' method
	}
// closing brace of the P40MyFirstApp class
}

